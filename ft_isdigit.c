#include "push_swap.h"

int	ft_isdigit(t_push *p, int c)
{
	if (c > 2147483647 || c < -2147483648)
	{
		write(1, "Error\n", 6);
		free_node(p, &p->list_a, &p->list_b);
		exit (1);
	}
	printf("HOOO\n");
	return (0);
}
